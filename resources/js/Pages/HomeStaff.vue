                            type="submit"
                        >
                            ลงทะเบียนโต๊ะ ( {{ this.table }} )
                        </button>
                        <button
                            @click="cancel_table(this.table)"
                            class="mx-2 bg-red-400 w-52 h-11 rounded-lg shadow-md text-white mb-3"
                        >
                            ยกเลิกโต๊ะ ( {{ this.table }} )
                        </button>
                    </div>
                </div>
            </form>
        </div>
    </ServicetableModal>
</template>

<script>
import { Link } from "@inertiajs/vue3";
import MenuModal from "@/components/MenuModal.vue";
import Registable from "@/components/MenuModal.vue";
import ServicetableModal from "@/components/ServicetableModal.vue";
import Sidennav from "@/components/Sidennav.vue";

export default {
    components: {
        Sidennav,
        MenuModal,
        Registable,
        ServicetableModal,
    },
    data() {
        return {
            isRegistable: false,
            isConfirmtable: false,
            isModalregistable: false,
            isCanceltable: false,
            isEdittable: false,
            isPayment: false,
            list_table: [],
            table: "",
            customer_adult: "",
            customer_children: "",
            customer_baby: "",
            main_package: "",
            package_main: "",
            secondary_package: "",
            package_secondary: "",
            status: "",
            new_table_number: "",
            editpackage_main: null,
            editpackage_secondary: null,
        };
    },
    methods: {
        getStatusText(status) {
            return status == 0 ? "( ว่าง )" : "( ไม่ว่าง )";
        },
        openservice(item, main_package_id, secondary_package_id, status) {
            if (status == 0) {
                this.isModalregistable = true;
                this.table = item;
                return;
            }
            if (status == 1) {
                this.table = item;
                this.status = status;

                this.editpackage_main = this.list_table.find(
                    (main_package) =>
                        main_package.package_main === main_package_id
                );

                this.editpackage_secondary = this.list_table.find(
                    (secondary_package) =>
                        secondary_package.package_secondary ===
                        secondary_package_id
                );

                this.editTable = this.list_table.find(
                    (list_table) => list_table.id === item
                );

                this.isEdittable = true;
                return;
            }

            if (status == 2) {
                this.table = item;
                this.status = status;

                this.editpackage_main = this.list_table.find(
                    (main_package) =>
                        main_package.package_main === main_package_id
                );

                this.editpackage_secondary = this.list_table.find(
                    (secondary_package) =>
                        secondary_package.package_secondary ===
                        secondary_package_id
                );

                this.isConfirmtable = true;
                return;
            }
        },
        choose_service(title, item) {
            if (title == "registable") {
                this.isRegistable = true;
                this.table = item;
            }
            if (title == "payment") {
                this.table = item;
            }
        },
        close(item, table) {
            if (item == "close_service") {
                this.table = "";
                this.isModalregistable = false;
            }
            if (item == "close_registable") {
                this.table = table;
                this.isRegistable = false;
                this.isModalregistable = true;
            }
            if (item == "close_edittable") {
                this.table = table;
                this.isEdittable = false;
            }
            if (item == "close_confirmtable") {
                this.table = table;
                this.isConfirmtable = false;
            }
        },
        fetch_main_package() {
            axios
                .get("/fetch_package")
                .then((response) => {
                    if (response.data != []) {
                        this.main_package = response.data;
                    }
                })
                .catch((error) => {
                    console.log(error);
                });
        },
        fetch_secondary_package() {
            axios
                .get("/fetch_secondary_package")
                .then((response) => {
                    if (response.data != []) {
                        this.secondary_package = response.data;
                    }
                })
                .catch((error) => {
                    console.log(error);
                });
        },
        fetch_table() {
            axios
                .get("/fetch_table")
                .then((response) => {
                    if (response.data != []) {
                        this.list_table = response.data;
                    }
                })
                .catch((error) => {
                    console.log(error);
                });
        },
        register_table() {
            const formData = new FormData();

            formData.append("shop_id", 1);
            formData.append("staff_id", 1);
            formData.append("table", this.table);
            formData.append("customer_adult", this.customer_adult);
            formData.append("customer_children", this.customer_children);
            formData.append("customer_baby", this.customer_baby);
            formData.append("main_package", this.package_main);
            formData.append("secondary_package", this.package_secondary);
            formData.append("status", 1);

            axios
                .post("/insert_bills", formData)
                .then((response) => {
                    console.log(response);
                    this.isRegistable = false;
                    this.isModalregistable = false;
                    this.isConfirmtable = false;
                    this.fetch_table();
                    this.table = "";
                    this.customer_adult = "";
                    this.customer_children = "";
                    this.customer_baby = "";
                    this.package_main = "";
                    this.package_secondary = "";
                })
                .catch((error) => {
                    console.log(error);
                });
        },
        edit_table() {
            const formData = new FormData();
            formData.append("shop_id", 1);
            formData.append("staff_id", 1);
            formData.append("table", this.table);
            formData.append("customer_adult", this.editTable.customer_adult);
            formData.append(
                "customer_children",
                this.editTable.customer_children
            );
            formData.append("customer_baby", this.editTable.customer_baby);
            formData.append("main_package", this.editpackage_main.package_main);
            formData.append(
                "secondary_package",
                this.editpackage_secondary.package_secondary
            );
            formData.append("status", 1);
            formData.append("new_table", this.new_table_number);

            axios
                .post("/edit_bills", formData)
                .then((response) => {
                    console.log(response);
                    this.isEdittable = false;
                    this.fetch_table();
                    this.table = "";
                    this.customer_adult = "";
                    this.customer_children = "";
                    this.customer_baby = "";
                    this.package_main = "";
                    this.package_secondary = "";
                    this.new_table_number = "";
                })
                .catch((error) => {
                    console.log(error);
                });
        },
        cancel_table(id) {
            const formData = new FormData();
            formData.append("table_id", id);
            axios
                .post("/close_bills", formData)
                .then((response) => {
                    console.log(response);
                    this.fetch_table();
                })
                .catch((error) => {
                    console.log(error);
                });
        },
        reserve_table() {
            const formData = new FormData();

            formData.append("shop_id", 1);
            formData.append("staff_id", 1);
            formData.append("table", this.table);
            formData.append("customer_adult", this.customer_adult);
            formData.append("customer_children", this.customer_children);
            formData.append("customer_baby", this.customer_baby);
            formData.append("main_package", this.package_main);
            formData.append("secondary_package", this.package_secondary);
            formData.append("status", 2);

            axios
                .post("/reserve_bills", formData)
                .then((response) => {
                    console.log(response);
                    this.isRegistable = false;
                    this.isModalregistable = false;
                    this.isConfirmtable = false;
                    this.fetch_table();
                    this.table = "";
                    this.customer_adult = "";
                    this.customer_children = "";
                    this.customer_baby = "";
                    this.package_main = "";
                    this.package_secondary = "";
                })
                .catch((error) => {
                    console.log(error);
                });
        },
        confirm_table() {
            const formData = new FormData();
            formData.append("shop_id", 1);
            formData.append("staff_id", 1);
            formData.append("table", this.table);
            formData.append("customer_adult", this.editTable.customer_adult);
            formData.append(
                "customer_children",
                this.editTable.customer_children
            );
            formData.append("customer_baby", this.editTable.customer_baby);
            formData.append("main_package", this.editpackage_main.package_main);
            formData.append(
                "secondary_package",
                this.editpackage_secondary.package_secondary
            );
            formData.append("status", 1);

            axios
                .post("/insert_bills", formData)
                .then((response) => {
                    console.log(response);
                    this.isRegistable = false;
                    this.isModalregistable = false;
                    this.isConfirmtable = false;
                    this.fetch_table();
                    this.table = "";
                    this.customer_adult = "";
                    this.customer_children = "";
                    this.customer_baby = "";
                    this.package_main = "";
                    this.package_secondary = "";
                })
                .catch((error) => {
                    console.log(error);
                });
        },
    },
    mounted() {
        this.fetch_table();
        this.fetch_main_package();
        this.fetch_secondary_package();
    },
};
</script>

